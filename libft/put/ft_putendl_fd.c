#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (!s)
		return ;
	while (s[i] != '\0')
	{
		(void)write(fd, &s[i], 1);
		i++;
	}
	(void)write(fd, "\n", 1);
}
